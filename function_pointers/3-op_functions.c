#include "3-calc.h"
/**
 * op_add - sums two intengers
 * @a: value
 * @b: value
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two intengers
 * @a: value
 * @b: value
 * Return: the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two intengers
 * @a: value
 * @b: value
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two intengers
 * @a: value
 * @b: value
 * Return: the result of the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculates the reminder of the division of two intengers
 * @a: value
 * @b: value
 * Return: the sum of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
